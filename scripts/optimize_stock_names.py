def clean_stock_name(name):
    """清理股票名称，去除动词前缀和冒号后的内容"""
    if not name:
        return name
    
    # 第一步：去除冒号及后面的所有内容
    if '：' in name:
        name = name.split('：')[0]
    if ':' in name:
        name = name.split(':')[0]
    
    # 第二步：去除常见的动词前缀（按长度降序排序，优先匹配更长的前缀）
    sorted_prefixes = sorted(VERB_PREFIXES, key=len, reverse=True)
    for prefix in sorted_prefixes:
        if name.startswith(prefix):
            # 去除前缀和可能的连接词
            cleaned = name[len(prefix):]
            # 去除开头的"的"、"之"等连接词
            cleaned = cleaned.lstrip('的之')
            if cleaned:
                return cleaned
    
    return name

def get_stock_code(name):
    """获取股票代码"""
    if name in STOCK_CODE_MAP:
        return STOCK_CODE_MAP[name]
    
    # 模糊匹配
    for stock_name, code in STOCK_CODE_MAP.items():
        if stock_name in name or name in stock_name:
            return code
    
    return "待补充"

def optimize_json_data(input_file, output_file):
    """优化JSON数据"""
    print(f"正在读取文件: {input_file}")
    
    with open(input_file, 'r', encoding='utf-8') as f:
        data = json.load(f)
    
    print(f"共读取 {len(data)} 条记录")
    
    # 用于合并同名股票
    stock_dict = {}
    
    for item in data:
        original_name = item.get('名称', '')
        if not original_name:
            continue
        
        # 清理名称
        cleaned_name = clean_stock_name(original_name)
        
        # 获取股票代码
        stock_code = get_stock_code(cleaned_name)
        
        # 更新item
        item['原始名称'] = original_name
        item['名称'] = cleaned_name
        item['代码'] = stock_code
        
        # 合并同名股票
        if cleaned_name not in stock_dict:
            stock_dict[cleaned_name] = item
        else:
            # 合并逻辑内容
            existing_logic = stock_dict[cleaned_name].get('逻辑', {})
            new_logic = item.get('逻辑', {})
            
            if isinstance(existing_logic, dict) and isinstance(new_logic, dict):
                for key in ['概况', '订单', '产品']:
                    if new_logic.get(key):
                        if existing_logic.get(key):
                            existing_logic[key] += '\n' + new_logic[key]
                        else:
                            existing_logic[key] = new_logic[key]
                stock_dict[cleaned_name]['逻辑'] = existing_logic
            elif isinstance(new_logic, str):
                if isinstance(existing_logic, str):
                    stock_dict[cleaned_name]['逻辑'] = existing_logic + '\n' + new_logic
                else:
                    if existing_logic.get('概况'):
                        existing_logic['概况'] += '\n' + new_logic
                    else:
                        existing_logic['概况'] = new_logic
                    stock_dict[cleaned_name]['逻辑'] = existing_logic
    
    # 转换回列表
    optimized_data = list(stock_dict.values())
    
    print(f"优化完成，共 {len(optimized_data)} 只唯一股票")
    
    # 统计
    code_stats = {"已补充": 0, "待补充": 0}
    for item in optimized_data:
        code = item.get('代码', '')
        if code == "待补充":
            code_stats["待补充"] += 1
        else:
            code_stats["已补充"] += 1
    
    print(f"\n股票代码统计:")
    print(f"  - 已补充: {code_stats['已补充']}")
    print(f"  - 待补充: {code_stats['待补充']}")
    
    # 保存
    print(f"\n正在保存到: {output_file}")
    with open(output_file, 'w', encoding='utf-8') as f:
        json.dump(optimized_data, f, ensure_ascii=False, indent=2)
    
    print("保存成功!")
    
    # 显示一些示例
    print("\n优化示例（前10条）:")
    for item in optimized_data[:10]:
        original = item.get('原始名称', '')
        cleaned = item.get('名称', '')
        code = item.get('代码', '')
        if original != cleaned:
            print(f"  {original} -> {cleaned} (代码: {code})")
        else:
            print(f"  {cleaned} (代码: {code})")

