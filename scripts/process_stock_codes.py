def get_stock_code(stock_name):
    """获取股票代码"""
    if stock_name in STOCK_CODE_MAP:
        return STOCK_CODE_MAP[stock_name]
    
    # 尝试简单的模糊匹配
    for name, code in STOCK_CODE_MAP.items():
        if stock_name in name or name in stock_name:
            return code
    
    return "待补充"

def process_json_file(input_file, output_file):
    """处理JSON文件"""
    print(f"正在读取文件: {input_file}")
    
    with open(input_file, 'r', encoding='utf-8') as f:
        data = json.load(f)
    
    print(f"共读取 {len(data)} 条记录")
    
    updated_count = 0
    for item in data:
        stock_name = item.get('名称', '')
        if stock_name:
            # 自动补充股票代码
            stock_code = get_stock_code(stock_name)
            item['代码'] = stock_code
            
            # 重新分类板块，添加"引用自"
            item['板块'] = f"一帆风顺研究院"
            
            updated_count += 1
    
    print(f"处理完成，共更新 {updated_count} 条记录")
    
    # 保存到新文件
    print(f"正在保存到: {output_file}")
    with open(output_file, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    
    print("保存成功!")
    
    # 统计
    code_stats = {"已补充": 0, "待补充": 0}
    for item in data:
        code = item.get('代码', '')
        if code == "待补充":
            code_stats["待补充"] += 1
        else:
            code_stats["已补充"] += 1
    
    print(f"\n股票代码统计:")
    print(f"  - 已补充: {code_stats['已补充']}")
    print(f"  - 待补充: {code_stats['待补充']}")
    
    # 显示待补充的股票
    pending = [item['名称'] for item in data if item.get('代码') == '待补充']
    if pending:
        print(f"\n无法补充代码的股票 ({len(pending)} 只):")
        for name in pending[:30]:
            print(f"  - {name}")
        if len(pending) > 30:
            print(f"  ... 还有 {len(pending) - 30} 只")

