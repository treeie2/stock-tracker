def get_stock_code(name):
    """获取股票代码"""
    if name in STOCK_CODE_MAP:
        return STOCK_CODE_MAP[name]
    
    # 模糊匹配
    for stock_name, code in STOCK_CODE_MAP.items():
        if stock_name in name or name in stock_name:
            return code
    
    return "待补充"

def convert_to_tracker_format(input_file, output_file):
    """转换为stock-tracker格式"""
    print(f"正在读取文件: {input_file}")
    
    with open(input_file, 'r', encoding='utf-8') as f:
        data = json.load(f)
    
    print(f"共读取 {len(data)} 条记录")
    
    stocks = []
    for idx, item in enumerate(data):
        name = item.get('名称', '')
        if not name:
            continue
        
        # 获取或更新股票代码
        code = get_stock_code(name)
        if code == "待补充":
            code = item.get('代码', '待补充')
        
        # 处理逻辑
        logic = item.get('逻辑', {})
        if isinstance(logic, dict):
            gk = logic.get('概况', '')
            dd = logic.get('订单', '')
            cp = logic.get('产品', '')
            logic_text = '\n'.join(filter(None, [gk, dd, cp]))
        else:
            logic_text = str(logic)
        
        # 获取来源
        source = item.get('引自', '一帆风顺研究院')
        date = item.get('日期', datetime.now().strftime('%Y-%m-%d'))
        
        # 创建股票ID
        stock_id = f"s_{int(datetime.now().timestamp()*1000)}_{idx}_{name[:4]}"
        
        # 创建记录
        record = {
            "id": f"rec_{int(datetime.now().timestamp()*1000)}_{idx}_{name[:4]}",
            "stockId": stock_id,
            "timestamp": datetime.now().strftime('%Y/%m/%d %H:%M:%S'),
            "customDate": date,
            "title": f"{date} {source}",
            "content": f"来源：{source}\n\n{logic_text}",
            "logic": f"来源：{source}\n\n{logic_text}",
            "dataPoints": [],
            "rawText": logic_text
        }
        
        # 估值
        valuation = item.get('估值', '')
        if valuation:
            record["dataPoints"].append({"label": "目标估值", "value": f"估值: {valuation}"})
        
        # 从产品中提取概念
        concepts = []
        if isinstance(logic, dict):
            cp = logic.get('产品', '')
            if cp:
                concepts = [f"概念: {p.strip()}" for p in cp.split('；') if p.strip()][:5]
        
        stock = {
            "id": stock_id,
            "name": name,
            "code": code,
            "price": "---",
            "dailyChange": "0%",
            "marketCap": "---",
            "peRatio": "---",
            "revenue": "---",
            "concepts": concepts if concepts else [source],
            "products": [],
            "sector": source,
            "targetValuation": f"估值: {valuation}" if valuation else "估值: -",
            "earningsForecast": "",
            "lastUpdated": datetime.now().strftime('%Y/%m/%d %H:%M:%S'),
            "records": [record]
        }
        
        stocks.append(stock)
    
    # 构建输出
    output = {"stocks": stocks}
    
    print(f"转换完成，共 {len(stocks)} 只股票")
    
    # 统计
    code_stats = {"已补充": 0, "待补充": 0}
    for stock in stocks:
        if stock["code"] == "待补充":
            code_stats["待补充"] += 1
        else:
            code_stats["已补充"] += 1
    
    print(f"\n股票代码统计:")
    print(f"  - 已补充: {code_stats['已补充']}")
    print(f"  - 待补充: {code_stats['待补充']}")
    
    # 保存
    print(f"\n正在保存到: {output_file}")
    with open(output_file, 'w', encoding='utf-8') as f:
        json.dump(output, f, ensure_ascii=False, indent=2)
    
    print("保存成功!")
    
    # 显示优化示例
    print("\n优化示例（前10条）:")
    for stock in stocks[:10]:
        print(f"  {stock['name']} ({stock['code']})")

