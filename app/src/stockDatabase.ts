// 创建名称到股票信息的映射
const nameToStockMap = new Map<string, StockBasicInfo>();
stockDatabase.forEach(stock => {
  nameToStockMap.set(stock.name, stock);
});

// 创建代码到股票信息的映射
const codeToStockMap = new Map<string, StockBasicInfo>();
stockDatabase.forEach(stock => {
  codeToStockMap.set(stock.symbol, stock);
});

/**
 * 通过股票名称查询股票信息
 */
export function searchStockByName(name: string): StockBasicInfo | null {
  if (!name) return null;
  
  const normalizedName = name.trim();
  
  // 精确匹配
  const exactMatch = nameToStockMap.get(normalizedName);
  if (exactMatch) return exactMatch;
  
  // 模糊匹配 - 名称包含
  for (const [stockName, stock] of nameToStockMap) {
    if (stockName.includes(normalizedName) || normalizedName.includes(stockName)) {
      return stock;
    }
  }
  
  return null;
}

/**
 * 通过股票代码查询股票信息
 */
export function searchStockByCode(code: string): StockBasicInfo | null {
  if (!code) return null;
  const normalizedCode = code.trim().replace(/[^0-9]/g, "") ;
  return codeToStockMap.get(normalizedCode) || null;
}

/**
 * 批量查询多个股票信息
 */
export function batchSearchStocks(names: string[]): Map<string, StockBasicInfo | null> {
  const result = new Map<string, StockBasicInfo | null>();
  
  for (const name of names) {
    result.set(name, searchStockByName(name));
  }
  
  return result;
}

/**
 * 获取所有股票数量
 */
export function getStockCount(): number {
  return stockDatabase.length;
}

/**
 * 搜索股票（支持名称或代码）
 */
export function searchStock(query: string): StockBasicInfo | null {
  if (!query) return null;
  
  // 如果是6位数字，按代码搜索
  if (/^\d{6}$/.test(query.trim())) {
    return searchStockByCode(query);
  }
  
  // 否则按名称搜索
  return searchStockByName(query);
}